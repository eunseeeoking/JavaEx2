package Day12;

public class Person {
	String name;
	int age;
	String addr;

	void Person() {
	}

	void Person(String name, int age) {
		this.name = name;
		this.age = age;

	}

	void Person(String name, int age, String addr) {
		this.name = name;
		this.age = age;
		this.addr = addr;
	}

}
